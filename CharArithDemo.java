//chuong trinh test thu kieu char
class CharArithDemo
{
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("gia tri ban dau cua ch la: " + ch);
        //có thể tăng value cho one biến kiểu char
        ch++;//tăng giá trị biến char
        System.out.println("Gia tri cua bien ch hien gio la: " + ch);
        //co the gan value int,long,short cho one bien kieu char
        ch = 90;//cung cap bien ch co value Z
        System.out.println("Gia tri cua bien ch hien gio la: " + ch);
    }
}